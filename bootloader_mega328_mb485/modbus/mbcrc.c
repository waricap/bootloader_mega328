/* ----------------------- System includes ----------------------------------*/


/* ----------------------- Platform includes --------------------------------*/
#include "port.h"


uint16_t usMBCRC16( const uint8_t *buf, uint16_t len )
{
	uint16_t crc = 0xFFFF;
	uint16_t i = 0;
	uint8_t bit = 0;

	for( i = 0; i < len; i++ )
	{
		crc ^= buf[i];

		for( bit = 0; bit < 8; bit++ )
		{
			if( crc & 0x0001 )
			{
				crc >>= 1;
				crc ^= 0xA001;
			}
			else
			{ crc >>= 1; }
		}
	}

	return crc;
}
